/**
 * Build-time script: fetch Japan prefectures TopoJSON, merge into the 9 JEPX
 * utility regions, project via d3-geo, emit `apps/web/src/lib/japan-region-paths.ts`
 * with pre-computed SVG path strings + viewBox.
 *
 * Run once after deps change:
 *   cd apps/web && node scripts/build-japan-paths.mjs
 *
 * Source: dataofjapan/land japan.topojson (MIT-licensed).
 * https://github.com/dataofjapan/land
 *
 * Output: zero geo deps at runtime. The 9 path strings + viewBox ship in the
 * bundle as a tiny static module.
 */

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { geoMercator, geoPath } from "d3-geo";
import { feature, merge } from "topojson-client";

const TOPOJSON_URL =
  "https://raw.githubusercontent.com/dataofjapan/land/master/japan.topojson";

// JIS X 0401 prefecture codes → JEPX utility region.
// Shizuoka (22) goes whole to TK in v1; the actual Fuji-river split is ignored.
const REGION_TO_PREFECTURES = {
  HK: { name: "Hokkaido", prefs: [1] },
  TH: { name: "Tohoku", prefs: [2, 3, 4, 5, 6, 7, 15] },
  TK: { name: "Tokyo", prefs: [8, 9, 10, 11, 12, 13, 14, 19, 22] },
  CB: { name: "Chubu", prefs: [20, 21, 23, 24] },
  HR: { name: "Hokuriku", prefs: [16, 17, 18] },
  KS: { name: "Kansai", prefs: [25, 26, 27, 28, 29, 30] },
  CG: { name: "Chugoku", prefs: [31, 32, 33, 34, 35] },
  SK: { name: "Shikoku", prefs: [36, 37, 38, 39] },
  KY: { name: "Kyushu", prefs: [40, 41, 42, 43, 44, 45, 46, 47] },
};

const VIEW_WIDTH = 800;
const VIEW_HEIGHT = 800;

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const OUT_PATH = resolve(__dirname, "../src/lib/japan-region-paths.ts");

async function main() {
  console.log(`Fetching ${TOPOJSON_URL} ...`);
  const res = await fetch(TOPOJSON_URL);
  if (!res.ok) {
    throw new Error(`fetch failed: ${res.status} ${res.statusText}`);
  }
  const topo = await res.json();

  // Find the prefectures object key — historically called "japan".
  const objectKey = Object.keys(topo.objects)[0];
  if (!objectKey) throw new Error("topojson has no objects");
  const prefObject = topo.objects[objectKey];
  console.log(`Using topojson object: ${objectKey}`);

  // Collect all prefecture geometries to find the projection bounds.
  const allFeatures = feature(topo, prefObject);
  const allCollection = {
    type: "FeatureCollection",
    features: allFeatures.features,
  };

  // Fit a Mercator projection inside our target viewBox.
  const projection = geoMercator().fitSize(
    [VIEW_WIDTH, VIEW_HEIGHT],
    allCollection,
  );

  // Custom path context that rounds coordinates to 1dp — full d3 precision
  // produces ~2 MB of path strings; 1dp cuts the bundle ~5× with no visible
  // loss at our viewBox scale.
  // Integer-only path coordinates (viewBox is 800×800 — sub-pixel detail is
  // invisible at any practical render size). Also dedupes runs of equal
  // L points to drop the "lineTo same place" no-ops.
  const round = (n) => Math.round(n);
  function makeRoundingContext() {
    let buf = "";
    let lx = NaN, ly = NaN;
    return {
      moveTo(x, y) {
        const rx = round(x), ry = round(y);
        buf += "M" + rx + "," + ry;
        lx = rx; ly = ry;
      },
      lineTo(x, y) {
        const rx = round(x), ry = round(y);
        if (rx === lx && ry === ly) return;
        buf += "L" + rx + "," + ry;
        lx = rx; ly = ry;
      },
      arc() { /* unused for polygons */ },
      closePath() { buf += "Z"; lx = NaN; ly = NaN; },
      result() { const out = buf; buf = ""; lx = NaN; ly = NaN; return out; },
    };
  }
  const ctx = makeRoundingContext();
  const pathGen = geoPath(projection, ctx);

  // For each utility region, filter the prefecture geometries by id, merge into
  // one MultiPolygon, then render to an SVG path string.
  const regions = [];
  for (const [code, { name, prefs }] of Object.entries(REGION_TO_PREFECTURES)) {
    const matching = prefObject.geometries.filter(
      (g) => prefs.includes(g.properties?.id),
    );
    if (matching.length === 0) {
      console.warn(`! ${code}: no matching prefectures for ${prefs.join(",")}`);
      regions.push({ code, name, d: "" });
      continue;
    }
    const merged = merge(topo, matching);
    pathGen({ type: "Feature", geometry: merged, properties: {} });
    const d = ctx.result();
    if (!d) {
      console.warn(`! ${code}: merge produced empty path`);
    }
    regions.push({ code, name, d });
    console.log(
      `✓ ${code} (${name}) ← ${prefs.length} prefectures, path len=${(d ?? "").length}`,
    );
  }

  const out = `/**
 * GENERATED by apps/web/scripts/build-japan-paths.mjs — DO NOT EDIT.
 * Re-run: \`cd apps/web && node scripts/build-japan-paths.mjs\`.
 *
 * Pre-projected SVG path strings for the 9 JEPX utility regions.
 * Mercator projection fit inside ${VIEW_WIDTH}×${VIEW_HEIGHT} viewBox.
 * Source: dataofjapan/land (MIT).
 */

export type RegionCode = "HK" | "TH" | "TK" | "CB" | "HR" | "KS" | "CG" | "SK" | "KY";

export type RegionPath = {
  code: RegionCode;
  name: string;
  /** SVG path \`d\` attribute. */
  d: string;
};

export const VIEW_BOX = { x: 0, y: 0, w: ${VIEW_WIDTH}, h: ${VIEW_HEIGHT} } as const;

export const REGION_PATHS: readonly RegionPath[] = ${JSON.stringify(regions, null, 2)} as const;
`;

  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, out, "utf8");
  console.log(`\nWrote ${OUT_PATH}`);
  console.log(`9 region paths total.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
